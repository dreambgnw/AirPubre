/**
 * ランダムにN個の単語を選んでマスターキーを生成する
 * @param {number} count - 単語数（デフォルト12）
 * @returns {string[]} 選ばれた単語の配列
 */
export function generateMasterKeyWords(count = 12) {
  const array = new Uint32Array(count)
  crypto.getRandomValues(array)
  return Array.from(array).map(n => WORD_LIST[n % WORD_LIST.length])
}

/**
 * 単語配列をキー文字列に変換
 * @param {string[]} words
 * @returns {string}
 */
export function wordsToKeyString(words) {
  return words.join('・')
}

/**
 * キー文字列を単語配列に変換
 * @param {string} keyString
 * @returns {string[]}
 */
export function keyStringToWords(keyString) {
  return keyString.split('・')
}
